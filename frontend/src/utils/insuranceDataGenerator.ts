interface Customer {
  customerName: string;
  birthdate: string;
  city: string;
  vehiclePower: number;
  voucher: number;
  priceMatch: number;
  commercialDiscount: boolean;
  agentsDiscount: boolean;
  vipDiscount: boolean;
  strongCarSurcharge: boolean;
  bonusProtection: boolean;
  autoLiability: boolean;
  glassProtection: boolean;
}

const generateInsuranceData = (): Customer[] => {
  const insurances: Customer[] = [];

  for (let i = 0; i < 5; i++) {
    const customer: Customer = {
      customerName: `Customer ${i + 1}`,
      birthdate: getRandomDate(new Date(1970, 0, 1), new Date(2002, 11, 31)),
      city: getRandomCity(),
      vehiclePower: getRandomNumber(50, 300),
      voucher: getRandomNumber(0, 100),
      priceMatch: getRandomNumber(0, 500),
      commercialDiscount: getRandomBoolean(),
      agentsDiscount: getRandomBoolean(),
      vipDiscount: getRandomBoolean(),
      strongCarSurcharge: getRandomBoolean(),
      bonusProtection: getRandomBoolean(),
      autoLiability: getRandomBoolean(),
      glassProtection: getRandomBoolean(),
    };

    insurances.push(customer);
  }
  return insurances;
};

export default generateInsuranceData;

function getRandomDate(start: Date, end: Date): string {
  return new Date(
    start.getTime() + Math.random() * (end.getTime() - start.getTime())
  ).toLocaleDateString();
}

function getRandomCity(): string {
  const cities: string[] = [
    "New York",
    "Los Angeles",
    "Chicago",
    "Houston",
    "Phoenix",
    "Philadelphia",
    "San Antonio",
    "San Diego",
    "Dallas",
    "San Jose",
    "Austin",
    "Jacksonville",
    "San Francisco",
    "Charlotte",
    "Indianapolis",
    "Seattle",
    "Denver",
    "Washington",
    "Boston",
    "El Paso",
  ];
  return cities[Math.floor(Math.random() * cities.length)];
}

function getRandomNumber(min: number, max: number): number {
  return Math.floor(Math.random() * (max - min + 1) + min);
}

function getRandomBoolean(): boolean {
  return Math.random() < 0.5;
}

